def gen_index(k, _i):
    if k[_i-1] == k[_i]:
        k[_i] += 1
        n = k[_i]
        while _i+1 <= -1:
            k[_i+1] = n
            _i += 1
        return k
    elif k[_i-1] < k[_i]:
        return gen_index(k, _i-1)

